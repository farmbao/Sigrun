    @Test
    public void testSerialize() {
        Serializer<TextHeader> serializer = new TextHeaderSerializer();
        TextHeader header = serializer.deserialize(SERIALIZED_TEXT_HEADER);
        byte[] serializedHeader = serializer.serialize(header);

        Assert.assertArrayEquals(SERIALIZED_TEXT_HEADER, serializedHeader);
    }

    @Test
    public void testDeserialize() {
        Serializer<TextHeader> serializer = new TextHeaderSerializer();
        TextHeader header = serializer.deserialize(SERIALIZED_TEXT_HEADER);

        for (int i = 0; i < TextHeader.PUNCH_CARD_AMOUNT; i++) {
            if (!(header.getCard(i).getCardNumber() + header.getCard(i).getCardContent()).equals(DESERIALIZED_HEADER[i])) {
                Assert.fail("Fail on position " + i);
            } else
                Assert.assertTrue(true);
        }
    }

